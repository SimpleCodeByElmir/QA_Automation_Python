### Math Operations
def my_mult(a,b):
  return a*b
def my_div(a,b):
  return a/b
def my_sum(a,b):
  return a+b
def my_sub(a,b):
  return a-b

def test_my_mult():
  assert (my_mult(10,5)) == 50
def test_my_div():
  assert (my_div(10,5)) == 3
def test_my_sum():
  assert (my_sum(10,5)) == 15
def test_my_sub():
  assert (my_sub(10,5)) == 5
